export const NavbarButton = ({
  href,
  as: Tag = "a",
  children,
  className,
  variant = "primary",
  ...props
}: {
  href?: string;
  as?: React.ElementType;
  children: React.ReactNode;
  className?: string;
  variant?: "primary" | "secondary" | "dark" | "gradient";
} & (
  | React.ComponentPropsWithoutRef<"a">
  | React.ComponentPropsWithoutRef<"button">
)) => {
  const baseStyles =
    "px-4 py-2 rounded-md bg-white button bg-white text-black text-sm font-bold relative cursor-pointer hover:-translate-y-0.5 transition duration-200 inline-block text-center";

const variantStyles: Record<string, string> = {
    primary: `
      bg-gradient-to-r from-indigo-600 to-purple-600
      hover:from-indigo-500 hover:to-purple-500
      text-white
      shadow-indigo-500/25 hover:shadow-indigo-500/40
    `,

    gradient: `
      bg-gradient-to-r from-pink-600 to-fuchsia-600
      hover:from-pink-500 hover:to-fuchsia-500
      text-white
      shadow-pink-500/30 hover:shadow-pink-500/50
      hover:scale-[1.02]
    `,

    secondary: `
      bg-white/10 backdrop-blur-sm
      border border-white/20
      text-white
      hover:bg-white/15
      shadow-none hover:shadow-lg hover:shadow-white/10
    `,

    danger: `
      bg-gradient-to-r from-red-600 to-rose-600
      hover:from-red-500 hover:to-rose-500
      text-white
      shadow-red-500/30 hover:shadow-red-500/50
    `,

    ghost: `
      bg-transparent
      text-gray-300 hover:text-white
      shadow-none
      hover:bg-white/10
    `,
  };

  return (
    <Tag
      href={href || undefined}
      className={cn(baseStyles, variantStyles[variant], className)}
      {...props}
    >
      {children}
    </Tag>
  );
};
